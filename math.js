function plus(number1, number2) {
    var result = Number(number1) + Number(number2);
    return result;
}
function minus(number1, number2) {
    var result = Number(number1) - Number(number2);
    return result;
}
function multiply(number1, number2) {
    var result = Number(number1) * Number(number2);
    return result;
}
function divide(number1, number2) {
    var result = Number(number1) / Number(number2);
    return result;
}
